class Location
	# -------------------------------------------------------------------------
	# Attributes...
	# -------------------------------------------------------------------------
	attr_reader :address, :city, :country

	# -------------------------------------------------------------------------
	# Public Methods...
	# -------------------------------------------------------------------------
	def to_s
		"#{@address}, #{@city}, #{@country}"
	end

	# -------------------------------------------------------------------------
	# Initialize...
	# -------------------------------------------------------------------------
	def initialize(an_address, a_city, a_country)
		@address = an_address
		@city = a_city
		@country = a_country
	end
end