class Antecedent < ActiveRecord::Base
  # -------------------------------------------------------------------------
  # Attributes...
  # -------------------------------------------------------------------------
  attr_accessible :description, :medical_history, :medical_history_id

  # -------------------------------------------------------------------------
  # Associations...
  # -------------------------------------------------------------------------
  belongs_to :medical_history

  # -------------------------------------------------------------------------
  # Validations...
  # -------------------------------------------------------------------------
  validates :description, presence: true
end
