class Role < ActiveRecord::Base
	# -------------------------------------------------------------------------
	# Public Methods...
	# -------------------------------------------------------------------------
	def to_s
		I18n.t "role.#{name}"
	end

	# -------------------------------------------------------------------------
	# Attributes...
	# -------------------------------------------------------------------------
	attr_accessible :name

	# -------------------------------------------------------------------------
	# Associations...
	# -------------------------------------------------------------------------
	has_many :assignments
	has_many :users, :through => :assignments
end
